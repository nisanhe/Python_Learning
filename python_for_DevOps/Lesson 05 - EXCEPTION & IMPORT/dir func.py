# what is dir?
# dir() is a built-in function in Python that returns a list of valid attributes for that object
# it can be used to see what methods and variables are available in a module, class, or
# object.
# dir() is often used in conjunction with the print() function to print out the list of attributes
# dir() is used to get a list of valid attributes for that object.


import random
x = dir(random)
print(x)
